package com.infopub.j4android.two.c23;

public class IfCondition {

	public static void main(String[] args) {
		int v=100;
		if(v%2==0){                     // 2로 나누었을 때 나머지가 0면 짝수다.
			System.out.println("짝수");
		}else{                            // 짝수가 아니면 홀수다.
			System.out.println("홀수");
		}
	}

}
